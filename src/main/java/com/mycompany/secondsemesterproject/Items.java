public boolean checkFields() {
    boolean checkPassed = true;
    String productName = ProductName.getText();
    String priceBought = PriceBought.getText();
    String priceSold = SellingPrice.getText();

    if (productName.isEmpty() || priceBought.isEmpty() || priceSold.isEmpty()) {
        // Handle empty fields
        ProductNameError.setVisible(productName.isEmpty());
        OnlyNumbersFirst.setVisible(priceSold.isEmpty() || Integer.parseInt(priceSold) == 0);
        OnlyNumbersSecond.setVisible(priceBought.isEmpty() || Integer.parseInt(priceBought) == 0);
        checkPassed = false;
    } else {
        try {
            int parsedPriceBought = Integer.parseInt(priceBought);
            int parsedPriceSold = Integer.parseInt(priceSold);

            // Check for conditions
            if (parsedPriceBought == 0) {
                OnlyNumbersFirst.setVisible(true);
                checkPassed = false;
            }
            if (parsedPriceSold == 0) {
                OnlyNumbersSecond.setVisible(true);
                checkPassed = false;
            }
            if (containsNumber(productName)) {
                ProductNameError.setVisible(true);
                checkPassed = false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter appropriate values to the fields");
            checkPassed = false;
        }
    }
    
    return checkPassed;
}

private boolean containsNumber(String str) {
    for (char c : str.toCharArray()) {
        if (Character.isDigit(c)) {
            return true;
        }
    }
    return false;
}

    
    
    
