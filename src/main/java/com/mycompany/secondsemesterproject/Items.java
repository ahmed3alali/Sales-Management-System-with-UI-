     private int getItemNumber(int categoryNumber) throws SQLException {
   int nextOrderNumber=0;
    String query = "SELECT MAX(ITEMCODE) AS maxitemcode FROM app.ITEMSINSYSTEM";
    PreparedStatement ps = con.prepareStatement(query);
    ResultSet rs = ps.executeQuery();
    if (rs.next()) {
        int maxOrderNumber = rs.getInt("maxitemcode");
        if (!rs.wasNull()) {
             nextOrderNumber = maxOrderNumber + 1;
        }
    }
    return nextOrderNumber;
}
    
    
    
    
    
