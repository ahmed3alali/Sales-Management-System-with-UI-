    public boolean isLoginValid(String username, String password) {
        boolean loginvalid= false;
        try {
            File file = new File("C:\\Users\\User\\Desktop\\CP2\\users.txt");
            Scanner scanner = new Scanner(file);
            
            while (scanner.hasNextLine()) {
                String line = scanner.nextLine();
                String[] parts = line.split(",");
                
                if (parts.length >= 5 && parts[0].equals(username) && parts[3].equals(password) && Boolean.parseBoolean(parts[4])) {
                  loginvalid= true;
                  
                   Manager m = new Manager();
                  
                    m.setVisible(true);
                    dispose();
                   
                  break; 
                  
                   
                } else if (parts.length >= 5 && parts[0].equals(username) && parts[3].equals(password) && !Boolean.parseBoolean(parts[4])) {
                loginvalid= true;
                    WelV1 w = new WelV1();
                    w.setVisible(true);
                    dispose();
                    
                    break;
                }  
              
               
            }
             if (!loginvalid) {
               JOptionPane.showMessageDialog(this, "wronginfo");
                   
               }
            scanner.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
        
       return loginvalid;
    }
    
    
     private boolean usernameExists(String username) {
        try {
            File file = new File("C:\\Users\\User\\Desktop\\CP2\\users.txt");
            BufferedReader reader = new BufferedReader(new FileReader(file));

            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length >= 1 && parts[0].equals(username)) {
                    reader.close();
                    return true; // Username exists
                }
            }
             reader.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
          return false;
     }
        
    
