    public boolean checkFields() {
    boolean checkPassed = true;
   String name = CreateNameFld.getText();
        String email = CreateEmlFld.getText();
        String emaily = ConfirmEmaily.getText();
        String mob = Mobile.getText();
        String password = PasswordFLD.getText();
        String confPassword = PasswordFLD2.getText();

    if (name.isEmpty() ||emaily.isEmpty() || mob.isEmpty()|| password.isEmpty()|| confPassword.isEmpty()) {
        FLDSness.setVisible(true);
        checkPassed = false;
    } else {
        try {
            
          
            if (containsNumber(name)) {
                CorrectChError.setVisible(true);
                checkPassed = false;
            }
            else if (!Pattern.matches("\\w+@\\w+\\.\\w+", email)) {
            IncorrectFormatEML.setVisible(true);
                
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter appropriate values to the fields");
            checkPassed = false;
        }
    }
    
    return checkPassed;
}

private boolean containsNumber(String str) {
    for (char c : str.toCharArray()) {
        if (Character.isDigit(c)) {
            return true;
        }
    }
    return false;
}
    

    
    
private boolean checker() {
    boolean matches = false;
    String emailEntered = CreateEmlFld.getText();
    String confirmEmail = ConfirmEmaily.getText();
    String password = PasswordFLD.getText();
    String conPass = PasswordFLD2.getText();
    String mobStr = Mobile.getText();
    
    // Check if the email and confirm email match
    if (!emailEntered.equals(confirmEmail)) {
        EmailsDONT.setVisible(true);
    }
    // Check if the password and confirm password match
    else if (!password.equals(conPass)) {
        PasswordDONT.setVisible(true);
    }
    // Check if the email format is valid
    else if (!emailEntered.matches("\\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}\\b")) {
        EmailsDONT.setVisible(true);
    }
    // Check if the mobile is valid (contains only digits)
    else if (mobStr.isEmpty() || !mobStr.matches("\\d+")) {
        MobDont.setVisible(true);
    }
    // All checks passed
    else {
        System.out.println("Checker Method Passed!");
        matches = true;
    }
    
    return matches;
}
    
    
    
 
     
    
     
    
    
    
    
    
    
